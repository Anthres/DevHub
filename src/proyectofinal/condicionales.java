    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String estratoInput = estrato.getText();
                int estrato = Integer.parseInt(estratoInput);

                String nivel;
                if (estrato >= 1 && estrato <= 5) {
                    switch (estrato) {
                        case 1:
                            nivel = "Bajo";
                            break;
                        case 2:
                            nivel = "Medio Bajo";
                            break;
                        case 3:
                            nivel = "Medio";
                            break;
                        case 4:
                            nivel = "Medio Alto";
                            break;
                        case 5:
                            nivel = "Alto";
                            break;
                        default:
                            nivel = "No aplica";
                            break;
                    }
                } else {
                    nivel = "No aplica";
                }

                tnivel.setText(nivel);
            

            
    }//GEN-LAST:event_jButton2ActionPerformed

